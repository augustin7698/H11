function H(str) {
	str = str.replace(new RegExp(/\s/g),"");
	str = str.replace(new RegExp(/[àáâãäå]/g),"a");
	str = str.replace(new RegExp(/æ/g),"ae");
	str = str.replace(new RegExp(/ç/g),"c");
	str = str.replace(new RegExp(/[èéêë]/g),"e");
	str = str.replace(new RegExp(/[ìíîï]/g),"i");
	str = str.replace(new RegExp(/ñ/g),"n");                
	str = str.replace(new RegExp(/[òóôõö]/g),"o");
	str = str.replace(new RegExp(/œ/g),"oe");
	str = str.replace(new RegExp(/[ùúûü]/g),"u");
	str = str.replace(new RegExp(/[ýÿ]/g),"y");

	// transfomer le text en tableau
	str = str.split("");

	// transfomer la phrases en suite de nombre
	for (var i = str.length - 1; i >= 0; i--) {
		// multiplier 
		if (i%2 == 0) {
			str[i] = letter.indexOf(str[i]) * 2 * Math.LOG10E;
		} else {
			str[i] = letter.indexOf(str[i]) * 2 * Math.LN2;
		}
		

		// ajouter au tableau quand les chiffre impaire moins ceux d'avants
		if (str[i]%2 == 0) {
			str.concat(str[i] - str[i - 1]);
		}
	}


	for (var i = str.length - 1; i >= 0; i--) {
		if (i == 0) {
			str[i] = str[i] + str[i];
		} else {
			str[i] = str[i] - str[i - 1];
		}
		
	}


	// additionner tout les membres du tableau
	str = eval(String(str).replaceAll(",", "+").replaceAll(" ", "+").replaceAll("-", ""));


	str = String(str).replace(".", "");
	str = Number(str).toString(36);

	return str;
}